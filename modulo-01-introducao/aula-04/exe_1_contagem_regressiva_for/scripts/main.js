console.log("Contagem regressiva de 10 a 0");
for (let i = 10; i != -1; i--) {
    console.log(i);
}
console.log("Fim da contagem regressiva");

console.log("Contagem regressiva de 10 a 0");
for (let i = 10; i >= 0; i--) {
    console.log(i);
}
console.log("Fim da contagem regressiva");